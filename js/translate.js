function translate(
    to
) {
    var to = to.substring(0, 2);
    var translation = translations[to];
    if (window.$) {
        $('#idiomas a').removeClass('selected');
        $('#idiomas a[name=idioma_' + to + ']').addClass('selected');
    }
    for (var c in map_controls) {
        var theControl = map_controls[c];
        theControl.controlUI.title = translation['control_' + theControl.name].title;
        theControl.controlText.innerHTML = translation['control_' + theControl.name].text;
    };
    calculateMoney();
}

function getCookie(cname)
{
    var name = cname + "=";
    var ca = document.cookie.split(';');
    for(var i=0; i<ca.length; i++) 
    {
        var c = ca[i].trim();
        if (c.indexOf(name)==0) return c.substring(name.length,c.length);
    }
    return "";
}

